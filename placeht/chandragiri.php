      <h3>Introduction</h3>
      <p>
        Chandragiri is a municipality in Kathmandu District. The city’s main
        attractions include Chandragiri Hill, Nepal with its Cable Car.
        Chandragiri Hill is 16 kilometers from Central Kathmandu, lies on the
        South-West of Kathmandu Valley and is 2551 meters from sea level. It is
        one of the four hills that surround Kathmandu valley including Shivapuri
        (2732m), Phulchowki (2695m) and Nagarjun (2095m). Chandragiri can be
        reached via cable car, driving or hiking. The second cable car in Nepal
        after Manakamana was commenced through this route. The cable car route
        is around 2.5 km and it takes about 9 to 14 minutes depending on the
        wind speed on the cable car. It is a thrilling and fun-filled experience
        to see Kathmandu valley, the landscape and the mountains from the cable
        car. The hill provides panoramic views of Kathmandu valley and the
        Himalayan ranges from Annapurna to Everest. It is an easy one-day trip
        from Central Kathmandu Valley. Chandragiri Cable Car runs from Thankot
        to Chandragiri hills. The cable car system consists of 38 gondolas that
        can carry 1000 people per hour. Chandragiri Hills is one of the Nepal’s
        most loved day-trip destinations. It is also a famous hiking destination
        in Nepal. Walking around the hills provides a breath of fresh air, and
        you get to see the natural beauty of the country side and breath taking
        scenes. The main destination of Chandragiri Hill is the mountain top
        from where the spectacular views of the great Himalayan mountain ranges
        can be seen. The green valley of Kathmandu lies at the bottom where you
        can see the growing urbanization. Chandragiri hilltop is a major tourist
        attraction, with an excellent combination of natural beauty and cultural
        significance. The best time to explore and visit Chandragiri is during
        spring and fall season. In winter season one can sometimes experience
        snowfall as well. To fulfil the cultural diversity of the place, the
        Bhaleshwar Mahadev Temple stands at the Chandragiri Hill and is an
        extremely pristine shrine. The temple features two levels in the typical
        pagoda-influenced Nepalese architecture, wooden grid windows and wall
        carvings of different gods and goddesses. Outside the temple, there is a
        statue of King Prithvi Narayan Shah standing against the white mountain
        peaks in the distance. It maintains its historical importance as it was
        said that King Prithvi Narayan Shah prayed here before the unification
        of Nepal and conquered Kathmandu Valley. It is also believed that he
        meditated on the hilltop and got blessings from Bhaleshwar Mahadev for
        his devotion. After Prithvi Narayan Shah unified the country, he used
        Chandragiri Hill to be one of the four trade passes. Chandragiri Private
        Limited operates the cable car with recreation centers, restaurants and
        shops on the hilltop. There is a plan to build an amusement park,
        organize paragliding and zip flying adventures. You can find many shops
        that promote local and branded products here. There are other cafes and
        food stalls in and around the place. This place has been designed to be
        friendly for differently-abled people as well. The paths and pavements
        are made easier for the elderly to walk around without too much of
        stress and effort. This place has gained popularity among the locals as
        well as the tourists in a short span of time. It is a must visit place
        at least once for all the nature, history and culture lovers.
      </p>
      <h3>History</h3>
      <p>
        The Chandragiri view point used to be look out station for military
        during malla period in kathmandu. According to history, it was once used
        by late King Prithvi Narayan Shah for the conquest of Kathmandu leading
        to foundation of Mordern Nepal.
